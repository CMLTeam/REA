package com.cmlteam.opendatabot.model;

public class Head {
    private String name;
    private String role;

    public Head() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
